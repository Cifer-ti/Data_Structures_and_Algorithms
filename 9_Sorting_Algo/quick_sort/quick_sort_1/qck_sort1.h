#ifndef QCK_SORT_H
#define QCK_SORT_H

/**
 * qck_sort: Sorts an array in ascending order, using quick sort algo.
 * 
 * data[]- Pointer to the array to be sorted.
 * lo- The lower bound of array where sort starts.
 * hi- The upper bound of array where sorts ends.
 * 
*/
void qck_sort(int data[], int lo, int hi);

#endif